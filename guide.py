def get_guide(key: str | None = None) -> str:
    if key and key.lower() in GUIDES:
        g = GUIDES[key.lower()]
        return f"\U0001f4d6 {g['name']}\n\n{g['text']}"

    lines = ["\U0001f4d6 지표 해석 가이드\n"]
    for k, g in GUIDES.items():
        lines.append(f"\u25b6 {g['name']}")
        lines.append(g["text"])
        lines.append("")

    lines.append("\U0001f50d 개별 조회: /guide cape, /guide vix, /guide matrix 등")
    return "\n".join(lines)
